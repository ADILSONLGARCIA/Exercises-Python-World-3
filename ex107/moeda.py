def aumentar(valor, taxa):
    res = valor + (valor * taxa/100)
    return res

def diminuir(valor, taxa):
    res = valor - (valor * taxa/100)
    return res

def dobreo(valor):
    res = valor * 2
    return res

def metade(valor):
    res = valor / 2
    return res


